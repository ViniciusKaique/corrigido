#include "libft.h"

int	ft_str_is_numeric(const char *s)
{
	int	i;

	i = 0;
	if (!s || *s == '\0')
		return (0);
	if (s[i] == '-' || s[i] == '+')
		i++;
	if (s[i] == '\0')
		return (0);
	while (s[i] != '\0')
	{
		if (!(s[i] >= '0' && s[i] <= '9'))
		{
			return (0);
		}
		i++;
	}
	return (1);
}
